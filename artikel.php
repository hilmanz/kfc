                <strong><p>Di tahun 2011, Cinta Laura memutuskan pindah ke Amerika Serikat untuk menuntut ilmu di salah satu perguruan tinggi di New York. </p>
				<p>Di tengah kesibukannya, belum lama ini KFC Music mewawancari Cinta mengenai beberapa kegiatan terbarunya selama di Amerika Serikat.</p>
				<p>Silahkan menyimak:</p></strong>
                <ol>
                	<li><p><span class="red">Katanya habis liburan ya? Bagaimana cerita tentang liburan Cinta?</span></p>
<p>Iya aku baru aja libur Spring Break selama seminggu di London.</p>

<li><p><span class="red">Dengan siapa saja Cinta liburan dan cerita menarik apa saja selama liburan yang bisa dibagi?</span></p>
<p>Pengennya sih sama mami, tapi karena mami di Indonesia, terpaksa deh pergi sendiri. Disana aku ketemu sahabat waktu SD-SMP di The International School of Bogor and bff (best friend forever) waktu di The Jakarta International School yg namanya Anna.</p>

<p>I was very happy in London! Karena udaranya mulai hangat/ spring weather and daun2 mulai keluar dari cabang2 pohon. Sooooooo amazing! Belum lagi istana2 and gedung2 kuno nya bagus banget. Memang kebudayaan Eropa lebih menarik dari Amerika.</p></li>

<li><p><span class="red">Kok tidak liburan di Indonesia saja?</span></p>
<p>Liburnya cuma seminggu, jadi tanggung kalo pulang kampung ke Indonesia, nanti aja Summer Holiday liburnya hampir 3 bulan, kalo ngga ada shooting aku mau tinggal lama di Indonesia.</p></li>

<li><p><span class="red">Bagaimana dengan update aktivitas keartisan di Amerika? Album, shooting?</span></p>
<p>Rencananya mungkin aku mau casting film di Los Angeles, akhir Mei atau awal Juni, tapi kesempatan untuk dapet peran kan sulit banget, maklum lawannnya artis papan atas hollywood semua. Jadi chill aja deh! kalo dapet great, kalo ngga dapet ya liburan and promo album kedua di Indonesia.</p></li>

<li><p><span class="red">Dengar-dengar mau ujian juga ya? Persiapannya bagaimana?</span></p>
<p>Iya, awal Mei aku ujian akhir semester yang pasti aku sibuk and stress luar biasa. Pasti 24 jam ngga bakal keluar kampus and asrama. Mudah2an rambut aku ngga sampe rontok kebanyakan belajar.</p></li>

<li><p><span class="red">Oh iya, di Amerika katanya film The Raid jadi box office (menurut the hollywood reporter), kamu sendiri melihat euforia film nasional tersebut ngga disana? Apa ikut nonton juga?</span></p>
<p>Belum Box Office, tapi dapet sambutan yang sangat baik di 3 Festifal Film disana:, Sundance Film Festival di Utah, SXSW Film Festival di Texas, MOMA ND/NF Film Festival di New York.</p>

<p>Aku belum sempat nonton karena sibuk ngerjain tugas and test setiap hari, tapi aku liat cuplikannya di google (imdb The Raid) masuk rank 500, berarti prestasi yang sangat bagus untuk Film Indonesia. We can be proud of it!</p></li>

<li><p><span class="red">Di Amerika, apakah Cinta mengikuti perkembangan dunia hiburan tanah air? Dari mana atau dari siapa? Yang Cinta tau bagaimana?</span></p>
