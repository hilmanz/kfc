            <div class="content">
            	<p>Marcello Tahitoe lebih dikenal dengan nama Ello, adalah pria kelahiran Jakarta, 20 Februari 1983. Ia berprofesi sebagai penyanyi di dunia dan telah merilis dua album.</p>

                <p>Ello sendiri adalah anak bungsu dari tiga bersaudara pasangan musikus Minggus Tahitoe dan penyanyi Diana Nasution. </p>
                
                <p>Debut album Mahasiswa Diploma Periklanan - FISIP UI ini berjudul namanya sendiri dirilis pada 28 Februari 2005 dan masuk dalam deretan album terlaris. Hit singlenya Pergi Untuk Kembali yang merupakan karya ayahnya telah membukukan angka penjualan lebih dari 150 ribu kopi, dan mendapatkan sertifikat Platinum. </p>
                
                <p>Eksistensi Ello semakin kuat saat Anugerah Musik Indonesia (AMI) Award 2005 memberikan dua penghargaan untuk album dan dirinya, kategori Album Terbaik dan Pendatang Baru Terbaik.</p>
                
                <p>Setelah tiga tahun vakum, Ello kembali merilis album keduanya dengan single andalan Masih Ada pada awal 2009. Dari segi musik, nuansa album keduanya lebih ke arah pop alternatif daripada album pertamanya. Di tahun 2012 ini Ello merilis album bertajuk  TAUB MUMU kalau dibaca terbalik adalah BUAT UMUM. Artinya jelas, album ini diperuntukkan untuk siapa saja, baik Joni n Cindy (sebutan penggemar Ello) atau siapapun yang menyukai musik Ello.</p>
                
                <p>Album Taub Mumu ini berisi 11 lagu dengan mengandalkan single baru berjudul Gak Kayak Mantanmu. lagu berirama middle-tempo ini dibawakan dengan petikan gitar yang ringan namun semakin lama ketukannya semakin intens. lagu-lagu lain yang tak kalah enak untuk dinikmati adalah Yang Ku nanti, Selagi Bimbang, Aku Selalu Ada, Jangan Kau sesali, Trauma, Hadapi Dengan Senyuman, Seperti Dulu, Inilah Aku, Kita Bisa Bersama, dan ditutup dengan Ayo indonesia Bisa, yang menjadi theme song Sea Games 2011.</p>
                
                <p style="font-size:20px;">Album TAUB MUMU sudah bisa didapatkan di seluruh gerai KFC <br />
                di Indonesia mulai bulan Maret 2012.</p>

